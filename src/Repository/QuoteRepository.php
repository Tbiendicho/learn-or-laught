    public function findAllQuotesByCategory($id)
    {
        $entityManager = $this->getEntityManager();

        // We will use the DQL (Doctrine Query Language)
        $query = $entityManager->createQuery(
            'SELECT q, c
            FROM App\Entity\Quote q
            LEFT JOIN q.category c

        -- this parameter will forbid some DQL injections
            WHERE c.id LIKE :id'
        )->setParameter('id', $id);
        
        // returns the selected Quote Object
        return $query->getResult();
        
    }

